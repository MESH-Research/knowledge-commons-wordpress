<h2>Welcome to ACH 2021!</h2>

<p>ACH is the United States-based constituent organization in the Alliance for Digital Humanities Organizations (ADHO). <a href="https://ach2021.ach.org" target="_blank">The ACH 2021 conference</a> provides a forum for conversations on an expansive definition of digital humanities in a broad array of subject areas, methods, and communities of practice.</p>
                            
<p>ACH 2021 will take place online via Zoom and Humanities Commons, 21-23 July 2021. [<a href="https://hcommons.org/groups/ach-2021/forum/topic/welcome-to-ach-2021/#post-45823">read more&#8230;</a>]</p>

<p><strong>Here&#8217;s a quick overview of what you need to know:</strong></p>
<ul>
<li>All participants and attendees of this virtual conference must adhere to the <a href="https://ach2021.ach.org/ach-conference-code-of-conduct/" target="_blank">Code of Conduct</a>. If you witness or experience a violation of this Code of Conduct, this page provides information for reporting the incident.</li>
<li><a href="https://members.ach.org/civicrm/event/info/?reset=1&id=10" target="_blank">Registration</a> is open through Thursday, 15 July.</li>
<li><a href="https://hcommons.org/docs/ach-2021-schedule/">The conference schedule</a> will be posted here, including links for Zoom events, which will appear close to the event start time.</li>
<li>Asynchronous presentations, as well as post-event discussions, will be held in the <a href="https://hcommons.org/groups/ach-2021/forum/">discussion forum</a>. Here&#8217;s <a href="https://hcommons.org/docs/how-to-use-the-discussion-forum/">a guide on how to use the discussion forum</a>.</li>
<li>If you need help with Zoom, you might consult <a href="https://support.zoom.us/hc/en-us/articles/206618765-Zoom-Video-Tutorials">their tutorials</a>, and particularly the tutorial on <a href="https://youtu.be/hIkCmbvAHQQ">joining a Zoom call</a>.</li>
<li>If you have questions, email us at ach2021@ach.org. We'll try to gather questions that get asked frequently and add them to our <a href="https://hcommons.org/docs/faq">FAQ</a>!</li>
</ul>

